package Operadores_Relacionais;

public class exemplo {

    public static void main(String[] args) {
        int valor1, valor2;

        valor1 = 10;
        valor2 = 7;

        System.out.println(valor1 == valor2);
        System.out.println(valor1 != valor2);
        System.out.println(valor1 > valor2);
        System.out.println(valor1 < valor2);
        System.out.println(valor1 >= valor2);
        System.out.println(valor1 <= valor2);
    }
}
